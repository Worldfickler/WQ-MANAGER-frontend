// 获取国旗SVG文件路径
export function getCountryFlagPath(countryCode: string): string {
  const fileName = countryFlagFiles[countryCode]
  if (fileName) {
    return new URL(`../assets/flags/${fileName}.svg`, import.meta.url).href
  }
  // 默认返回地球图标
  return new URL(`../assets/flags/earth.svg`, import.meta.url).href
}
